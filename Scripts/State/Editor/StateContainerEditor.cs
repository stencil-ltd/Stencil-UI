using State.Containers;
using UnityEditor;
using UnityEngine;

namespace State.Editor
{
    [CustomEditor(typeof(StateContainer<>), true)]
    public class StateContainerEditor : StencilDataEditor
    {
        public override void OnInspectorGUI()
        {
            base.OnInspectorGUI();
            if (GUILayout.Button("Notify Changed"))
                (target as INotifiable)?.Notify();
        }
    }
}